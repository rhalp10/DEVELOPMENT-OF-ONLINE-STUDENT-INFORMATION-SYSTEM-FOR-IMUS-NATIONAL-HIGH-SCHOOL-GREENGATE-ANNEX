<h3><strong>How will K to 12 affect the college curriculum?</strong></h3>

<ul id="ui-accordion-accordion-panel-3">
	<li>The College General Education curriculum will have fewer units. Subjects that have been taken up in Basic Education will be removed from the College General Education curriculum.</li>
	<li>Details of the new GE Curriculum may be found in&nbsp;<a href="http://www.ched.gov.ph/wp-content/uploads/2013/07/CMO-No.20-s2013.pdf" target="_blank">CHED Memorandum Order No. 20, series of 2013</a>.</li>
</ul>

<p>&nbsp;</p>

<h3>I&rsquo;ve been hearing that a lot of people have not been consulted regarding K to 12. Is this true?</h3>

<ul id="ui-accordion-accordion-panel-4">
	<li>DepEd has always been transparent in the planning and implementation of K to 12. There have been regular consultations with various sectors since 2010, before the law was passed, during the crafting of the IRR, and during implementations. DepEd representatives have also attended various fora and conferences, including legislative inquiries, regarding K to12. We are open to criticisms and suggestions regarding this.</li>
</ul>

