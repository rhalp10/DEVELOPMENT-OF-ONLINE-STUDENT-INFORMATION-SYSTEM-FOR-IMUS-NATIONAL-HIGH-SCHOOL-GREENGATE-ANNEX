                      <h3><strong>What is the K to 12 Program?</strong></h3>

<p>The K to 12 Program covers 13 years of basic education with the following key stages:</p>

<ul>
	<li>Kindergarten to Grade 3</li>
	<li>Grades 4 to 6</li>
	<li>Grades 7 to 10 (Junior High School)</li>
	<li>Grades 11 and 12 (Senior High School)</li>
</ul>

<h3><strong>Why are we now implementing 12 years of basic education?</strong></h3>

<ul>
	<li>The Philippines is the last country in Asia and one of only three countries worldwide with a 10-year pre-university cycle (Angola and Djibouti are the other two).</li>
	<li>A 12-year program is found to be the best period for learning under basic education. It is also the recognized standard for students and professionals globally.</li>
</ul>

<p>&nbsp;</p>

<h3><strong>What has been done to get ready for K to 12? Are we really ready for K to 12?</strong></h3>

<ul id="ui-accordion-accordion-panel-2">
	<li>SY 2011-2012: Universal Kindergarten implementation begins</li>
	<li>SY 2012-2013: Enhanced curriculum for Grades 1-7 implemented</li>
	<li>2013: K to 12 enacted into Law</li>
	<li>2014: Curriculum for Grades 11-12 finished
	<hr /></li>
</ul>

<ul>
	<li>This 2015, we are getting ready for the implementation of Senior High School (SHS) in SY 2016-2017.</li>
	<li>We are on the fifth year of the implementation of the K to 12 Program. Our last mile is the Senior High School. All 221 divisions of the Department of Education (DepEd) have finished planning and have figures on enrolment a year in advance. These plans were reviewed by a separate team and finalized upon consultation with other stakeholders.<br />
	&nbsp;
	<ul>
		<li><strong>Classrooms:</strong>&nbsp;DepEd has built 66,813 classrooms from 2010 to 2013. There are 33,608 classrooms completed and undergoing construction in 2014. As of DepEd is planning to establish 5,899 Senior High Schools nationwide. As of April 30, 2015, DepEd has issued provisional permits to 1,866 private schools set to offer Senior High School in 2016.</li>
		<li><strong>Teachers:</strong>&nbsp;From 2010-2014, DepEd has filled 128,105 new teacher items. DepEd is targeting two kinds of teachers: those who will teach the core subjects, and those who will teach the specialized subjects per track. DepEd will hire 37,000 teachers for Senior High School for 2016 alone.</li>
		<li><strong>Textbooks:</strong>&nbsp;Learning materials are being produced for elementary to junior high while textbooks for Senior High School (which has specialized subjects) are being bid out.</li>
		<li><strong>Curriculum:</strong>&nbsp;The K to 12 curriculum is standards- and competence-based. It is inclusive and built around the needs of the learners and the community. The curriculum is done and is available on the DepEd website. It is the first time in history that the entire curriculum is digitized and made accessible to the public.</li>
	</ul>
	</li>
	<li><strong>Private SHS:</strong>&nbsp;There are 2,199 private schools cleared to offer Senior High School and over 200 more being processed.</li>
</ul>

